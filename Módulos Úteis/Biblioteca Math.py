import math

math.sqrt(9)  # Raiz quadrada
print(math.sqrt(9))

math.sin(45)  # Seno
print(math.sin(45))

math.cos(45)  # Cosseno
print(math.cos(45))

math.log(1000, 10)  # Logaritmo
print(math.log(1000, 10))

"""se não colocar a base ele vai calcular o logaritmo natural (base e) de euler"""
math.e  # Constante de Euler
print(math.e)


math.pi  # Constante Pi
print(math.pi)


math.pow(2, 3)  # Potência
print(math.pow(2, 3))

math.ceil(2.5)  # Arredonda para cima
print(math.ceil(2.5))


math.floor(2.5)  # Arredonda para baixo
print(math.floor(2.5))


math.trunc(2.5)  # Trunca o número
print(math.trunc(2.5))
